		<p>The Mars Society, created in August of 1998, is an organization dedicated to the colonization of the red planet. Unlike other teams and research 
		groups vying for the opportunity to put a human on Mars, they actually have a plan on bringing the astronauts back to Earth. To accomplish this, they’ve 
		set up a hypothetical system in which the explorers utilize the martian surface to develop the fuel needed for the return trip, and exploit the orbits of 
		the two planets to vastly reduce the time and energy needed for a return trip by a scale of six. Another factor on their side, they’ve already got a test 
		site, set up in the south of Utah, and have plans for a 1-year simulated mission in the Canadian high arctic, christened the “Mars Arctic 365 Mission”. 
		As their president Robert Zubrin pointed out during a press conference: <em>“We are much closer today to being able to send humans to Mars than we were to 
		being able to send men to the Moon in 1961, and we were there eight years later. Given the will, we could have humans on Mars within a decade.”</em></p>
