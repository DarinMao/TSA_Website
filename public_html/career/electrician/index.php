			<p>To get a job for NASA, experts from NASA itself recommend training as part of a degree in another related field, such as a technician or repairer. It's also a good idea to spend several years working for a large company (i.e. Dominion, IBM, General Electric). If you prefer a path outside of the corporate world, jobs in the military for electricians are abundant, and can confer prestige and add a certain level of strength to any potential. In addition, jobs with companies such as Lockheed Martin and Boeing have the benefits of being military-related, while still being a corporate entity.</p>
