		<p>Chemists find new ways to make space exploration cheaper and more efficient.</p>
		<section>
			<p>In the growing field of aerospace, the future chemists and scientists will likely not be performing any of the experiments. However, robots and landers will be doing the dirty work, while scientists in control stations and offices will instead be designing new and innovative tests to be done on Martian soil. </p>
			<p>Probably the most famous (or infamous) experiments done in this fashion was by the 1976 Viking lander. It scooped up a sample of soil, then added a solution of amino acids and other nutrient molecules that were radiolabelled with C14, an experiment drawn-up by NASA chemists. After a few hours, the sample began gave off CO2 radiolabelled with the same Carbon, indicating that the molecules from Earth were being broken down in by something in the soil, likely something alive.</p>
			<p>Unfortunately for this experiment, it probably wasn’t all it was cracked up to be. When  more organic molecules were added a week later, they failed create another spike in CO2, something that should have happened if the purported life forms had multiplied. As of today, scientists believe that inorganic salts in the Martian soil, such as superoxide containing O2 ions, were responsible for the observed oxidation, as opposed to biological action.</p>
			<p>In the future, chemists will likely be of high demand in potential Martian colonies. Besides being of high demand for performing experiments, they could provide a lifesaving support in case of potential natural disasters or chemical contaminants. Whatever the reason, chemists will sure to be vital in any and all future settlements on the Red Planet.</p>
