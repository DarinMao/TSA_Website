			<p>Like most people on Earth, astronauts get weekends off. Even if it's not a weekend, crew members can have fun like we do. They can watch movies, read books, play games, etc. Fun is an essential part of life, and without it life is very boring. Having fun gives the astronauts a break from work, keeping them interested, active, and focused in their job. And with a job as important and as dangerous as theirs, staying focused is more important than ever. </p>
