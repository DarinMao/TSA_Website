			<header><h3>Exercise</h3></header>
			<p>The conditions of space are very different from those on Earth. These differences can cause severe health effects. To prevent these effects, astronauts must exercise every day. On average, astronauts exercise two hours every day. </p>
			<p>The exercise equipment astronauts use is very different from the types used on Earth. This is done to account for the difference in gravity, among other things. </p>
