			<header><h3>Sleep</h3></header>
			<p>Astronauts are humans, too, and they need to sleep, just like on Earth. Astronauts are scheduled for eight hours of sleep each day. Astronauts sleep in small cabins, which are large enough to hold one person. Because there is no (or at least very little) gravity in space, they must sleep in sleeping bags attached to the cabin. This way they will not float around and bump into things. </p>
