			<header><h3>Jobs</h3></header>
			<p>Astronauts perform actions that require human input, and cannot be done automatically by computers and machines. They also monitor automated actions, to make sure that they are being done correctly. As of today (2017) there are not many tasks that computers can do just as well as humans; computers just aren't advanced enough. Having humans operating machinery makes designing the spacecraft much easier. Even though humans aren't as fast or accurate, and make more mistakes, we can handle those mistakes much better, should they arise. </p>
			<p>Humans can't survive very well on their own in space. One of the jobs of the astronauts is to monitor the life support systems, to make sure that they are working properly. Without life support systems, the astronauts would not live very long. </p>	
